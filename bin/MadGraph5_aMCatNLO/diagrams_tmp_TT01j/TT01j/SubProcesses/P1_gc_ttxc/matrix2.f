     $   *SMALL_WIDTH_TREATMENT)), MDL_WH)
        FK_MDL_WT = SIGN(MAX(ABS(MDL_WT), ABS(MDL_MT
     $   *SMALL_WIDTH_TREATMENT)), MDL_WT)
        FK_ZERO = SIGN(MAX(ABS(ZERO), ABS(MDL_MC*SMALL_WIDTH_TREATMENT)
     $   ), ZERO)
        FK_MDL_WZ = SIGN(MAX(ABS(MDL_WZ), ABS(MDL_MZ
     $   *SMALL_WIDTH_TREATMENT)), MDL_WZ)
        FK_ZERO = SIGN(MAX(ABS(ZERO), ABS(ZERO*SMALL_WIDTH_TREATMENT))
     $   , ZERO)
      ENDIF


      CALL VXXXXX(P(0,1),ZERO,NHEL(1),-1*IC(1),W(1,1))
      CALL OXXXXX(P(0,2),MDL_MC,NHEL(2),-1*IC(2),W(1,2))
      CALL OXXXXX(P(0,3),MDL_MT,NHEL(3),+1*IC(3),W(1,3))
      CALL IXXXXX(P(0,4),MDL_MT,NHEL(4),-1*IC(4),W(1,4))
      CALL IXXXXX(P(0,5),MDL_MC,NHEL(5),-1*IC(5),W(1,5))
      CALL FFV2_2(W(1,5),W(1,1),GC_623,MDL_MC, FK_ZERO,W(1,6))
      CALL FFV1P0_3(W(1,4),W(1,3),GC_2,ZERO, FK_ZERO,W(1,7))
C     Amplitude(s) for diagram number 1
      CALL FFV1_0(W(1,6),W(1,2),W(1,7),GC_2,AMP(1))
      CALL FFV1_3_3(W(1,4),W(1,3),GC_264,GC_203,MDL_MZ, FK_MDL_WZ,W(1
     $ ,8))
C     Amplitude(s) for diagram number 2
      CALL FFV1_3_0(W(1,6),W(1,2),W(1,8),GC_264,GC_203,AMP(2))
      CALL FFV1P0_3(W(1,4),W(1,3),GC_6,ZERO, FK_ZERO,W(1,9))
C     Amplitude(s) for diagram number 3
      CALL FFV1_0(W(1,6),W(1,2),W(1,9),GC_6,AMP(3))
      CALL FFS2_3(W(1,4),W(1,3),GC_1025,MDL_MH, FK_MDL_WH,W(1,10))
C     Amplitude(s) for diagram number 4
      CALL FFS2_0(W(1,6),W(1,2),W(1,10),GC_572,AMP(4))
      CALL FFV1_2(W(1,5),W(1,1),GC_6,MDL_MC, FK_ZERO,W(1,6))
      CALL FFV2P0_3(W(1,4),W(1,3),GC_357,ZERO, FK_ZERO,W(1,11))
