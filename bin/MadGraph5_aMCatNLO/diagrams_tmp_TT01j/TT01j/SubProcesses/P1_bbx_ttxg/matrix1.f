      DATA (CF(I,  1),I=  1,  4) /   12,    4,    4,    0/
C     1 T(2,1) T(5,3,4)
      DATA DENOM(2)/1/
      DATA (CF(I,  2),I=  1,  4) /    4,   12,    0,    4/
C     1 T(2,4) T(5,3,1)
      DATA DENOM(3)/1/
      DATA (CF(I,  3),I=  1,  4) /    4,    0,   12,    4/
C     1 T(3,1) T(5,2,4)
      DATA DENOM(4)/1/
      DATA (CF(I,  4),I=  1,  4) /    0,    4,    4,   12/
C     1 T(3,4) T(5,2,1)
C     ----------
C     BEGIN CODE
C     ----------
      IF (FIRST) THEN
        FIRST=.FALSE.
        FK_MDL_WW = SIGN(MAX(ABS(MDL_WW), ABS(MDL_MW
     $   *SMALL_WIDTH_TREATMENT)), MDL_WW)
        FK_ZERO = SIGN(MAX(ABS(ZERO), ABS(MDL_MB*SMALL_WIDTH_TREATMENT)
     $   ), ZERO)
        FK_MDL_WZ = SIGN(MAX(ABS(MDL_WZ), ABS(MDL_MZ
     $   *SMALL_WIDTH_TREATMENT)), MDL_WZ)
        FK_MDL_WT = SIGN(MAX(ABS(MDL_WT), ABS(MDL_MT
     $   *SMALL_WIDTH_TREATMENT)), MDL_WT)
        FK_MDL_WH = SIGN(MAX(ABS(MDL_WH), ABS(MDL_MH
     $   *SMALL_WIDTH_TREATMENT)), MDL_WH)
        FK_ZERO = SIGN(MAX(ABS(ZERO), ABS(ZERO*SMALL_WIDTH_TREATMENT))
     $   , ZERO)
      ENDIF


      CALL IXXXXX(P(0,1),MDL_MB,NHEL(1),+1*IC(1),W(1,1))
      CALL OXXXXX(P(0,2),MDL_MB,NHEL(2),-1*IC(2),W(1,2))
      CALL OXXXXX(P(0,3),MDL_MT,NHEL(3),+1*IC(3),W(1,3))
      CALL IXXXXX(P(0,4),MDL_MT,NHEL(4),-1*IC(4),W(1,4))
      CALL VXXXXX(P(0,5),ZERO,NHEL(5),+1*IC(5),W(1,5))
      CALL FFV2P0_3(W(1,1),W(1,2),GC_337,ZERO, FK_ZERO,W(1,6))
      CALL FFV1P0_3(W(1,4),W(1,3),GC_6,ZERO, FK_ZERO,W(1,7))
C     Amplitude(s) for diagram number 1
      CALL VVV5_0(W(1,6),W(1,7),W(1,5),GC_7,AMP(1))
      CALL FFV1P0_3(W(1,1),W(1,2),GC_6,ZERO, FK_ZERO,W(1,8))
      CALL FFV2P0_3(W(1,4),W(1,3),GC_351,ZERO, FK_ZERO,W(1,9))
C     Amplitude(s) for diagram number 2
      CALL VVV5_0(W(1,8),W(1,9),W(1,5),GC_7,AMP(2))
C     Amplitude(s) for diagram number 3
      CALL VVV5_0(W(1,8),W(1,7),W(1,5),GC_7,AMP(3))
C     Amplitude(s) for diagram number 4
      CALL VVV8_0(W(1,8),W(1,7),W(1,5),GC_32,AMP(4))
C     Amplitude(s) for diagram number 5
      CALL VVV3_0(W(1,8),W(1,7),W(1,5),GC_478,AMP(5))
C     Amplitude(s) for diagram number 6
      CALL VVV7_0(W(1,8),W(1,7),W(1,5),GC_31,AMP(6))
      CALL FFV9P0_3(W(1,4),W(1,3),GC_352,ZERO, FK_ZERO,W(1,10))
C     Amplitude(s) for diagram number 7
      CALL VVV5_0(W(1,8),W(1,10),W(1,5),GC_7,AMP(7))
      CALL FFS2_3(W(1,4),W(1,3),GC_1025,MDL_MH, FK_MDL_WH,W(1,11))
C     Amplitude(s) for diagram number 8
      CALL VVS2_0(W(1,8),W(1,5),W(1,11),GC_348,AMP(8))
C     Amplitude(s) for diagram number 9
      CALL VVS4_0(W(1,8),W(1,5),W(1,11),GC_347,AMP(9))
      CALL FFV9P0_3(W(1,1),W(1,2),GC_338,ZERO, FK_ZERO,W(1,12))
C     Amplitude(s) for diagram number 10
      CALL VVV5_0(W(1,12),W(1,7),W(1,5),GC_7,AMP(10))
      CALL FFS2_3(W(1,1),W(1,2),GC_566,MDL_MH, FK_MDL_WH,W(1,13))
C     Amplitude(s) for diagram number 11
      CALL VVS2_0(W(1,7),W(1,5),W(1,13),GC_348,AMP(11))
C     Amplitude(s) for diagram number 12
      CALL VVS4_0(W(1,7),W(1,5),W(1,13),GC_347,AMP(12))
